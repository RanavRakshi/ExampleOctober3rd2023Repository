package com.sgtesting.singledarrayassignments1;

public class BooleanArrayForLoop {

	public static void main(String[] args) {
		//8) WAP to create boolean array and read the elements from boolean array using for loop?
		
		// create a boolean array
		boolean a[]={true,false,true,true,false};
		
		// read the boolean array using for loop
		for(int i=0; i<a.length;i++)
		{
			System.out.println(a[i]);
		}

	}

}
